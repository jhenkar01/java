public class IncrementDecrementExample1 {
 public static void main(String args[]){
   int num = 87;
   System.out.println("the intialize no is"+num);
   int postincrement = num++;
   System.out.println("the post increment value is:"+postincrement);
   int postdecrement = num--;
   System.out.println("the post decrement value is:"+postdecrement);
  
  }
}